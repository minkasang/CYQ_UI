// 加载任务清单（从 window.共享任务清单数据 读取，该数据由 任务拆分/任务数据.js 提供）
function 加载任务清单() {
  console.log('[看板数据] 开始加载任务清单...');
  const data = window.共享任务清单数据;
  if (!data || !data.aiTasks) {
    console.warn('[看板数据] 未找到共享任务清单数据，任务为空');
    return;
  }
  console.log('[看板数据] 读取到', data.aiTasks.length, '个任务');
  
  // 清空现有数据（用 .splice 而非 = []，保持 Vue 响应式引用）
  看板数据.待办.阻塞.splice(0);
  看板数据.待办.进行.splice(0);
  看板数据.待办.待处理.splice(0);
  看板数据.待办.已完成.splice(0);
  console.log('[看板数据] 已清空待办数据');
  
  // 转换 AI 任务为待办格式
  if (data.aiTasks) {
    data.aiTasks.forEach(task => {
      const todo = {
        编号: `A${task.id}`,
        标题: task.name,
        描述: task.lastUpdate || '',
        优先级: task.priority === '高' ? '高' : (task.priority === '中' ? '中' : '低'),
        分类: task.category
      };
      
      if (task.status === '已完成') {
        看板数据.待办.已完成.push({
          编号: todo.编号,
          标题: todo.标题,
          时间: task.lastUpdate || '06-14',
          内容: '任务完成'
        });
      } else if (task.status === '进行中') {
        看板数据.待办.进行.push(todo);
      } else if (task.status === '阻塞' || task.status === '移交') {
        看板数据.待办.阻塞.push({
          ...todo,
          建议: task.lastUpdate || '',
          等待天数: 3,
          来源: '共享任务清单'
        });
      } else if (task.status === '待开始' || task.status === '需定期执行' || task.status === '持续' || task.status === '待对方完成') {
        看板数据.待办.待处理.push(todo);
      }
    });
  }
  
  // 加载最近动态（取最新的5条已完成任务）
  看板数据.最近动态 = 看板数据.待办.已完成.slice(0, 5).map(t => ({
    时间: t.时间,
    内容: t.标题,
    阶段: '完成'
  }));
  
  // 添加今天的动态
  看板数据.最近动态.unshift({
    时间: "06-14",
    内容: "看板v3.1完成：6个Bug修复 + 调参面板 + 启动入口更新",
    阶段: "框架"
  });
  
  console.log('[看板数据] 任务清单加载完成:', 
    `阻塞${看板数据.待办.阻塞.length} 进行${看板数据.待办.进行.length} 待处理${看板数据.待办.待处理.length} 已完成${看板数据.待办.已完成.length}`
  );
}

